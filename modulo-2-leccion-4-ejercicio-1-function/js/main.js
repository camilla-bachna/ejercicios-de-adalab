'use strict';

function sum(a, b){
    return a * b;
}

const costKiwi = sum (2, 5);
console.log("5 kilos of kiwis cost", costKiwi);

const costOrange = sum (3, 3);
console.log('3 kilos of oranges cost', costOrange);

const costApple = sum (1, 4);
console.log('4 kilos of apples cost', costApple);